# ---------------------------------------------------------------------------
# GELU LUT tables
# GELU is implemented as: x * sigmoid(1.702 * x)
# Stage-13 LUT evaluates sigmoid(1.702*x) to the output buffer, then an EW mul
# stage multiplies by the original x in-place.
# ---------------------------------------------------------------------------

_GELU_SIGMOID_DOMAIN = 16.0
_GELU_SIGMOID_PRESCALE = 16384.0 / _GELU_SIGMOID_DOMAIN


def _fp16_bits(value: float) -> int:
    return int(np.array([np.float16(value)], dtype=np.float16).view(np.uint16)[0])


GELU_LE_TABLE = []
for _i in range(513):
    _x = -_GELU_SIGMOID_DOMAIN + (_i / 512.0) * _GELU_SIGMOID_DOMAIN
    _sig = 1.0 / (1.0 + math.exp(-1.702 * _x))
    GELU_LE_TABLE.append(min(0x7FFF, int(round(_sig * 32768.0))))

GELU_LO_TABLE = []
for _i in range(513):
    _x = (_i / 512.0) * _GELU_SIGMOID_DOMAIN
    _sig = 1.0 / (1.0 + math.exp(-1.702 * _x))
    GELU_LO_TABLE.append(min(0x7FFF, int(round(_sig * 32768.0))))

GELU_LUT_PARAMS = dict(
    bn_mul_cfg=_fp16_bits(_GELU_SIGMOID_PRESCALE) << 16,
    ew_cfg=0x00000300,
    out_cvt_offset=0x00000001,
    out_cvt_shift=0x0000F000,
    lut_le_slope_scale=0,
    lut_le_slope_shift=0,
    lut_lo_slope_scale=0,
    lut_lo_slope_shift=0,
)


