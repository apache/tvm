for _i in range(513):
    _x = _i / 512.0 * 16384.0 / _RSQRT_PRESCALE
    _val = 1.0 / np.sqrt(_x + _RSQRT_EPS) / _RSQRT_OUT_SCALE * 32768.0
    RSQRT_LO_TABLE.append(min(int(round(_val)), 0x7FFF))

# LE table (negative domain): saturate high (rsqrt undefined for x<0)
RSQRT_LE_TABLE = [0x7FFF] * 513

RSQRT_LUT_PARAMS = dict(
    bn_mul_cfg=0x70000000,    # prescale = 8192.0 (FP16 0x7000)
    ew_cfg=0x00000300,
    out_cvt_offset=0x00000001,
    out_cvt_shift=0x0000A000,  # minus_exp=10: table stores rsqrt(x)*1024, output = table/1024 = rsqrt(x)
    lut_le_slope_scale=0,
    lut_le_slope_shift=0,
    lut_lo_slope_scale=0,
    lut_lo_slope_shift=0,
)


# ---------------------------------------------------------------------------
# Reciprocal LUT tables
# For softmax normalization: 1/x for x in [0, max_domain].
# Default tables use max_domain=64 (sufficient for most attention heads).
# OUT_CVT minus_exp=12: output = table_value / 4096.
# Table scale = 4096 so output ≈ 1/x directly.
#
# Reference: rknpu-compiler-wt2/npu/backend/lut_tables.py
# ---------------------------------------------------------------------------

_RECIP_LUT_SCALE = 4096
_RECIP_MAX_DOMAIN = 64.0
_RECIP_PRESCALE = 16384.0 / _RECIP_MAX_DOMAIN  # = 256.0

# LO table (positive domain, 513 entries): 1/x for x in [0, 64]
RECIPROCAL_LO_TABLE = []
for _i in range(513):
    _x = (_i / 512.0) * _RECIP_MAX_DOMAIN
    if _x < 1e-6:
        RECIPROCAL_LO_TABLE.append(0x7FFF)
    else:
        RECIPROCAL_LO_TABLE.append(min(int(round(_RECIP_LUT_SCALE / _x)), 0x7FFF))

# LE table (negative domain): all zeros (reciprocal undefined for x<0 in this context)
RECIPROCAL_LE_TABLE = [0] * 513

RECIPROCAL_LUT_PARAMS = dict(
    bn_mul_cfg=0x5C000000,    # prescale = 256.0 (FP16 0x5C00)
    ew_cfg=0x00000302,
    out_cvt_offset=0x00000001,
    out_cvt_shift=0x0000C000,  # minus_exp=12
    lut_le_slope_scale=0,
    lut_le_slope_shift=0,
    lut_lo_slope_scale=0,
    lut_lo_slope_shift=0,
)


def build_reciprocal_tables(max_domain: float):
    """Build reciprocal LUT tables/params for a positive domain.

    Parameters
    ----------
    max_domain : float
        Maximum expected reciprocal input value.
    """
    domain = float(max(1.0, max_domain))
    prescale = 16384.0 / domain
    lo_table = []
    for i in range(513):
        x = (i / 512.0) * domain
        if x < 1e-6:
            lo_table.append(0x7FFF)
        else:
            lo_table.append(min(int(round(_RECIP_LUT_SCALE / x)), 0x7FFF))
    le_table = [0] * 513
    params = dict(RECIPROCAL_LUT_PARAMS)
    params["bn_mul_cfg"] = _fp16_bits(prescale) << 16
    return le_table, lo_table, params
