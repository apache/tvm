"""NPU LUT (Lookup Table) data for activation functions.

Pre-computed lookup tables for the RK3588 NPU DPU LUT unit.
Each table has 513 entries covering a piecewise-linear approximation
of the target function.

Tables are uploaded to the hardware via gen_lut_upload() and used by
gen_lut_eval() to evaluate the function on NPU.

Reference: rknpu-compiler-wt2/npu/backend/lut_tables.py
"""

import math

import numpy as np

# ---------------------------------------------------------------------------
# Exp LUT tables
# BN prescale = 2048.0 (FP16 0x6800), bn_mul_cfg = 0x68000000
# Maps input domain [-8, 8] to LUT index range [-16384, 16384].
#
# Hardware treats LUT entries as signed int16: values >= 0x8000 wrap negative
# and get clipped to 0.  Use scale 4096 so all entries stay in [0, 0x7FFF]
# while maximizing the representable exp range (up to ~8.0, covering inputs
# up to ~2.08).  OUT_CVT minus_exp=12 compensates: output ~ table_value / 4096.
# ---------------------------------------------------------------------------

EXP_BN_MUL_CFG = 0x68000000  # BN prescale for exp()
EXP_OUT_CVT_SHIFT = 0x0000C000  # minus_exp=12 (compensates scale=4096)

_EXP_LUT_SCALE = 4096

# LE covers negative domain: exp(x) for x in [-8, 0], 513 entries.
EXP_LE_TABLE = []
for _i in range(513):
    _x = -8.0 + (_i / 512.0) * 8.0
    EXP_LE_TABLE.append(min(0x7FFF, int(round(np.exp(_x) * _EXP_LUT_SCALE))))

# LO covers positive domain: exp(x) for x in [0, 8], 513 entries.
EXP_LO_TABLE = []
for _i in range(513):
    _x = (_i / 512.0) * 8.0
    EXP_LO_TABLE.append(min(0x7FFF, int(round(np.exp(_x) * _EXP_LUT_SCALE))))

assert EXP_LE_TABLE[512] == 4096    # exp(0) = 1.0 * 4096
assert EXP_LO_TABLE[0] == 4096      # exp(0) = 1.0 * 4096

# Exp-specific LUT execute parameters
